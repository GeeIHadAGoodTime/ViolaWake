def collect_wav_files(*dirs: Path, exclude_dirs: list[str] | None = None) -> list[Path]:
    """Collect all WAV files from multiple directories."""
    files = []
    for d in dirs:
        if not d.exists():
            print(f"WARNING: Directory not found: {d}", file=sys.stderr)
            continue
        for f in sorted(d.rglob("*.wav")):
            # Skip excluded subdirectories
            if exclude_dirs:
                skip = False
                for excl in exclude_dirs:
                    if excl in str(f):
                        skip = True
                        break
                if skip:
                    continue
            files.append(f)
    return files


def train_and_evaluate(
    experiment_name: str,
    pos_dirs: list[Path],
    neg_dirs: list[Path],
    output_model: Path,
    eval_dir: Path,
    epochs: int = 60,
    neg_ratio: int = 5,
    hidden_dim: int = 64,
    patience: int = 12,
) -> dict:
    """Train MLP on given data and evaluate against clean eval set."""
    print(f"\n{'='*60}")
    print(f"EXPERIMENT {experiment_name}")
    print(f"{'='*60}")

    # Collect files
    pos_files = []
    for d in pos_dirs:
        pos_files.extend(collect_wav_files(d, exclude_dirs=["_excluded"]))
    neg_files = []
    for d in neg_dirs:
        neg_files.extend(collect_wav_files(d))

    print(f"Positive files: {len(pos_files)}")
    print(f"Negative files: {len(neg_files)}")

    if len(pos_files) < 5:
        print(f"ERROR: Too few positive files ({len(pos_files)})")
        return {"error": "too few positives"}
    if len(neg_files) < 5:
        print(f"ERROR: Too few negative files ({len(neg_files)})")
        return {"error": "too few negatives"}

    # Import training dependencies
    import torch
    import torch.nn as nn
    import torch.optim as optim
    from torch.utils.data import DataLoader, TensorDataset

    from openwakeword.model import Model as OWWModel
    from violawake_sdk._constants import CLIP_SAMPLES
    from violawake_sdk.audio import center_crop, load_audio
    from violawake_sdk.training.augment import AugmentationPipeline
    from violawake_sdk.training.losses import FocalLoss

    # Load OWW preprocessor
    print("Loading OWW preprocessor...")
    oww = OWWModel()
    preprocessor = oww.preprocessor
    if not hasattr(preprocessor, "onnx_execution_provider"):
        preprocessor.onnx_execution_provider = "CPUExecutionProvider"

    def _audio_to_embedding(audio: np.ndarray) -> np.ndarray | None:
        audio = center_crop(audio, CLIP_SAMPLES)
        audio_int16 = np.clip(audio, -1.0, 1.0)
        audio_int16 = (audio_int16 * 32767).astype(np.int16)
        if len(audio_int16) < CLIP_SAMPLES:
            audio_int16 = np.pad(audio_int16, (0, CLIP_SAMPLES - len(audio_int16)))
        else:
            audio_int16 = audio_int16[:CLIP_SAMPLES]
        try:
            embeddings = preprocessor.embed_clips(audio_int16.reshape(1, -1), ncpu=1)
            return embeddings.mean(axis=1)[0].astype(np.float32)
        except Exception:
            return None

    # Extract positive embeddings with augmentation
    print("Extracting positive embeddings (with augmentation)...")
    pipeline = AugmentationPipeline(seed=42)
    augment_factor = 8

    pos_embeddings = []
    pos_source_idx = []
    for file_idx, f in enumerate(pos_files):
        audio = load_audio(f)
        if audio is None:
            continue
        # Original
        emb = _audio_to_embedding(audio)
        if emb is not None:
            pos_embeddings.append(emb)
            pos_source_idx.append(file_idx)
        # Augmented variants
        variants = pipeline.augment_clip(audio, factor=augment_factor)
        for v in variants:
            emb = _audio_to_embedding(v)
            if emb is not None:
                pos_embeddings.append(emb)
                pos_source_idx.append(file_idx)
        if (file_idx + 1) % 200 == 0:
            print(f"  ... {file_idx+1}/{len(pos_files)} positive files processed")

    print(f"  Total positive embeddings: {len(pos_embeddings)}")

    # Extract negative embeddings
    print("Extracting negative embeddings...")
    neg_embeddings = []
    neg_source_idx = []
    for file_idx, f in enumerate(neg_files):
        emb_result = load_audio(f)
        if emb_result is None:
            continue
        emb = _audio_to_embedding(emb_result)
        if emb is not None:
            neg_embeddings.append(emb)
            neg_source_idx.append(file_idx)
        if (file_idx + 1) % 500 == 0:
            print(f"  ... {file_idx+1}/{len(neg_files)} negative files processed")

    # If we need more negatives, augment existing ones
    target_neg = len(pos_embeddings) * neg_ratio
    if len(neg_embeddings) < target_neg:
        print(f"  Augmenting negatives: {len(neg_embeddings)} -> {target_neg}")
        aug_pipeline = AugmentationPipeline(seed=99)
        neg_file_iter = 0
        while len(neg_embeddings) < target_neg and neg_file_iter < target_neg * 2:
            source_idx = neg_file_iter % len(neg_files)
            audio = load_audio(neg_files[source_idx])
            if audio is not None:
                variants = aug_pipeline.augment_clip(audio, factor=2)
                for v in variants:
                    if len(neg_embeddings) >= target_neg:
                        break
                    emb = _audio_to_embedding(v)
                    if emb is not None:
                        neg_embeddings.append(emb)
                        neg_source_idx.append(source_idx)
            neg_file_iter += 1

    print(f"  Total negative embeddings: {len(neg_embeddings)}")

    # Build dataset with group-aware split
    pos_tensor = torch.tensor(np.array(pos_embeddings), dtype=torch.float32)
    neg_tensor = torch.tensor(np.array(neg_embeddings), dtype=torch.float32)
    X = torch.cat([pos_tensor, neg_tensor], dim=0)
    y = torch.cat([
        torch.ones(len(pos_embeddings), 1),
        torch.zeros(len(neg_embeddings), 1),
    ], dim=0)
    embedding_dim = X.shape[1]

    # Group-aware 80/20 split
    rng = np.random.default_rng(42)
    unique_pos = sorted(set(pos_source_idx))
    rng.shuffle(unique_pos)
    n_val_pos = max(1, len(unique_pos) // 5)
    val_pos_set = set(unique_pos[:n_val_pos])

    unique_neg = sorted(set(neg_source_idx))
    rng.shuffle(unique_neg)
    n_val_neg = max(1, len(unique_neg) // 5)
    val_neg_set = set(unique_neg[:n_val_neg])

    neg_offset = len(pos_embeddings)
    val_idx = (
        [i for i, s in enumerate(pos_source_idx) if s in val_pos_set]
        + [neg_offset + i for i, s in enumerate(neg_source_idx) if s in val_neg_set]
    )
    train_idx = (
        [i for i, s in enumerate(pos_source_idx) if s not in val_pos_set]
        + [neg_offset + i for i, s in enumerate(neg_source_idx) if s not in val_neg_set]
    )
    rng.shuffle(train_idx)
    rng.shuffle(val_idx)

    X_train, y_train = X[train_idx], y[train_idx]
    X_val, y_val = X[val_idx], y[val_idx]

    print(f"  Train: {len(train_idx)} ({int(y_train.sum())} pos)")
    print(f"  Val: {len(val_idx)} ({int(y_val.sum())} pos)")

    train_loader = DataLoader(
        TensorDataset(X_train, y_train), batch_size=32, shuffle=True
    )
    val_loader = DataLoader(
        TensorDataset(X_val, y_val), batch_size=32, shuffle=False
    )

    # Build MLP (same architecture as production)
    model = nn.Sequential(
        nn.Linear(embedding_dim, hidden_dim),
        nn.ReLU(),
        nn.Dropout(0.3),
        nn.Linear(hidden_dim, hidden_dim // 2),
        nn.ReLU(),
        nn.Dropout(0.2),
        nn.Linear(hidden_dim // 2, 1),
        nn.Sigmoid(),
    )

    criterion = FocalLoss(gamma=2.0, alpha=0.75, label_smoothing=0.05)
    optimizer = optim.AdamW(model.parameters(), lr=1e-3, weight_decay=1e-4)
    scheduler = optim.lr_scheduler.CosineAnnealingLR(optimizer, T_max=epochs)

    # EMA
    ema_decay = 0.999
    ema_params = {n: p.data.clone() for n, p in model.named_parameters()}

    def _update_ema():
        for n, p in model.named_parameters():
            ema_params[n] = ema_decay * ema_params[n] + (1 - ema_decay) * p.data

    # Training loop
    print(f"\nTraining for up to {epochs} epochs (patience={patience})...")
    print(f"{'Epoch':>6} {'Train':>10} {'Val':>10} {'Best':>10}")
    print("-" * 40)

    best_val_loss = float("inf")
    best_epoch = 0
    no_improve = 0
    best_state = None
    best_ema = None
    t_start = time.monotonic()

    for epoch in range(1, epochs + 1):
        model.train()
        train_loss = 0.0
        n_batches = 0
        for bx, by in train_loader:
            optimizer.zero_grad()
            pred = model(bx)
            loss = criterion(pred, by)
            loss.backward()
            torch.nn.utils.clip_grad_norm_(model.parameters(), 1.0)
            optimizer.step()
            _update_ema()
            train_loss += loss.item()
            n_batches += 1
        scheduler.step()
        avg_train = train_loss / max(n_batches, 1)

        model.eval()
        val_loss = 0.0
        vn = 0
        with torch.no_grad():
            for bx, by in val_loader:
                pred = model(bx)
                loss = criterion(pred, by)
                val_loss += loss.item()
                vn += 1
        avg_val = val_loss / max(vn, 1)

        if avg_val < best_val_loss:
            best_val_loss = avg_val
            best_epoch = epoch
            no_improve = 0
            best_state = {k: v.clone() for k, v in model.state_dict().items()}
            best_ema = {k: v.clone() for k, v in ema_params.items()}
        else:
            no_improve += 1

        if epoch % 10 == 0 or epoch == 1 or no_improve == 0:
            marker = " *" if epoch == best_epoch else ""
            print(f"{epoch:>6} {avg_train:>10.4f} {avg_val:>10.4f} {best_val_loss:>10.4f}{marker}")

        if no_improve >= patience:
            print(f"\nEarly stop at epoch {epoch}. Best: {best_epoch}")
            break

    # Restore best + apply EMA
    if best_state:
        model.load_state_dict(best_state)
    if best_ema:
        for n, p in model.named_parameters():
            p.data.copy_(best_ema[n])

    train_time = time.monotonic() - t_start
    print(f"Training done in {train_time:.1f}s. Best epoch: {best_epoch}")

    # Export ONNX
    output_model.parent.mkdir(parents=True, exist_ok=True)
    model.eval()
    dummy = torch.zeros(1, embedding_dim)
    torch.onnx.export(
        model, dummy, str(output_model),
        input_names=["embedding"], output_names=["score"],
        dynamic_axes={"embedding": {0: "batch"}, "score": {0: "batch"}},
        opset_version=11,
    )
    print(f"Model saved: {output_model}")

    # Evaluate against clean eval set
    print(f"\nEvaluating against clean eval set: {eval_dir}")
    from violawake_sdk.training.evaluate import evaluate_onnx_model

    eval_results = evaluate_onnx_model(
        model_path=str(output_model),
        test_dir=str(eval_dir),
        threshold=0.50,
        dump_scores_csv=str(output_model.with_suffix(".scores.csv")),
    )

    # Compute additional metrics
    pos_scores = np.array(eval_results["tp_scores"])
    neg_scores = np.array(eval_results["fp_scores"])

    # Trained-phrases-only metrics (filter out "viola_wake_up" and "viola_please")
    scores_csv = output_model.with_suffix(".scores.csv")
    trained_pos_scores = []
    if scores_csv.exists():
        import csv
        with open(scores_csv) as f:
            reader = csv.DictReader(f)
            for row in reader:
                if row["label"] == "positive":
                    fname = row["file"].lower()
                    # Skip untrained phrases
                    if "viola_wake_up" in fname or "viola_please" in fname:
                        continue
                    trained_pos_scores.append(float(row["score"]))

    trained_pos_arr = np.array(trained_pos_scores) if trained_pos_scores else pos_scores

    # EER for trained phrases only
    from violawake_sdk.training.evaluate import find_optimal_threshold, compute_dprime
    opt_trained = find_optimal_threshold(trained_pos_arr, neg_scores)

    result = {
        "experiment": experiment_name,
        "n_pos_files": len(pos_files),
        "n_neg_files": len(neg_files),
        "n_pos_embeddings": len(pos_embeddings),
        "n_neg_embeddings": len(neg_embeddings),
        "best_epoch": best_epoch,
        "best_val_loss": best_val_loss,
        "training_time_s": round(train_time, 1),
        # All-phrase metrics
        "all_dprime": round(eval_results["d_prime"], 3),
        "all_auc": round(eval_results["roc_auc"], 4),
        "all_eer": round(eval_results["eer_approx"], 4),
        "all_frr_at_050": round(eval_results["frr"], 4),
        # Trained-phrase metrics
        "trained_dprime": round(float(compute_dprime(trained_pos_arr, neg_scores)), 3),
        "trained_eer": round(opt_trained["eer_approx"], 4),
        "trained_frr_at_050": round(opt_trained["optimal_frr"], 4),
        # Score stats
        "pos_mean": round(float(pos_scores.mean()), 4),
        "neg_mean": round(float(neg_scores.mean()), 4),
        "trained_pos_mean": round(float(trained_pos_arr.mean()), 4),
    }

    print(f"\n--- Results for Experiment {experiment_name} ---")
    for k, v in result.items():
        print(f"  {k}: {v}")

    return result


def evaluate_baseline(eval_dir: Path) -> dict:
    """Evaluate the baseline model for comparison."""
    print("\n" + "="*60)
    print("BASELINE (current production model)")
    print("="*60)

    from violawake_sdk.training.evaluate import (
        evaluate_onnx_model, find_optimal_threshold, compute_dprime,
    )
    import csv

    eval_results = evaluate_onnx_model(
        model_path=str(BASELINE_MODEL),
        test_dir=str(eval_dir),
        threshold=0.50,
        dump_scores_csv=str(EXPERIMENTS_DIR / "baseline.scores.csv"),
    )

    pos_scores = np.array(eval_results["tp_scores"])
    neg_scores = np.array(eval_results["fp_scores"])

    # Trained-phrases-only
    trained_pos_scores = []
    csv_path = EXPERIMENTS_DIR / "baseline.scores.csv"
    if csv_path.exists():
        with open(csv_path) as f:
            reader = csv.DictReader(f)
            for row in reader:
                if row["label"] == "positive":
                    fname = row["file"].lower()
                    if "viola_wake_up" in fname or "viola_please" in fname:
                        continue
                    trained_pos_scores.append(float(row["score"]))

    trained_pos_arr = np.array(trained_pos_scores) if trained_pos_scores else pos_scores
    opt = find_optimal_threshold(trained_pos_arr, neg_scores)

    return {
        "experiment": "BASELINE",
        "all_dprime": round(eval_results["d_prime"], 3),
        "all_auc": round(eval_results["roc_auc"], 4),
        "all_eer": round(eval_results["eer_approx"], 4),
        "all_frr_at_050": round(eval_results["frr"], 4),
        "trained_dprime": round(float(compute_dprime(trained_pos_arr, neg_scores)), 3),
        "trained_eer": round(opt["eer_approx"], 4),
        "pos_mean": round(float(pos_scores.mean()), 4),
        "neg_mean": round(float(neg_scores.mean()), 4),
        "trained_pos_mean": round(float(trained_pos_arr.mean()), 4),
    }


def print_comparison_table(results: list[dict]):
    """Print a formatted comparison table."""
    print("\n\n" + "="*80)
    print("EXPERIMENT COMPARISON TABLE")
    print("="*80)

    headers = ["Experiment", "EER(all)", "EER(trained)", "AUC", "d'(trained)", "FRR@0.50", "pos_mean", "neg_mean"]
    widths = [12, 10, 13, 8, 12, 10, 10, 10]

    header_line = "  ".join(h.center(w) for h, w in zip(headers, widths))
    print(header_line)
    print("-" * len(header_line))

    baseline = None
    for r in results:
        if r.get("experiment") == "BASELINE":
            baseline = r

    for r in results:
        name = r.get("experiment", "?")
        eer_all = r.get("all_eer", 0)
        eer_trained = r.get("trained_eer", 0)
        auc_val = r.get("all_auc", 0)
        dp_trained = r.get("trained_dprime", 0)
        frr_050 = r.get("all_frr_at_050", 0)
        pos_m = r.get("pos_mean", 0)
        neg_m = r.get("neg_mean", 0)

        # Delta from baseline
        delta = ""
        if baseline and name != "BASELINE":
            d_eer = eer_all - baseline.get("all_eer", 0)
            delta = f" ({d_eer:+.1%})" if abs(d_eer) > 0.001 else ""

        vals = [
            name[:12],
            f"{eer_all:.1%}{delta}",
            f"{eer_trained:.1%}",
            f"{auc_val:.4f}",
            f"{dp_trained:.2f}",
            f"{frr_050:.1%}",
            f"{pos_m:.3f}",
            f"{neg_m:.3f}",
        ]
        print("  ".join(v.center(w) for v, w in zip(vals, widths)))

    print()


def main():
    parser = argparse.ArgumentParser(description="ViolaWake retraining experiments")
    parser.add_argument(
        "--experiment", "-e",
        choices=["B", "C", "D", "E", "all", "baseline"],
        default="all",
        help="Which experiment to run (default: all). E = max data from all sources.",
    )
    parser.add_argument("--epochs", type=int, default=60)
    parser.add_argument("--patience", type=int, default=12)
    args = parser.parse_args()

    EXPERIMENTS_DIR.mkdir(parents=True, exist_ok=True)

    results = []

    # Always run baseline first
    baseline = evaluate_baseline(EVAL_DIR)
    results.append(baseline)

    experiments = args.experiment
    if experiments == "all":
        experiments = ["B", "C", "D", "E"]
    elif experiments == "baseline":
        experiments = []
    else:
        experiments = [experiments]

    if "B" in experiments:
        # Experiment B: Existing data + confusable-word hard negatives
        if not NEW_CONFUSABLE_NEGATIVES.exists():
            print(f"ERROR: Confusable negatives not found at {NEW_CONFUSABLE_NEGATIVES}")
            print("Run generate_training_data.py first.")
        else:
            result_b = train_and_evaluate(
                "B_confusable",
                pos_dirs=[EXISTING_POSITIVES],
                neg_dirs=[EXISTING_NEGATIVES, NEW_CONFUSABLE_NEGATIVES],
                output_model=EXPERIMENTS_DIR / "models" / "exp_b_confusable.onnx",
                eval_dir=EVAL_DIR,
                epochs=args.epochs,
                patience=args.patience,
            )
            results.append(result_b)

    if "C" in experiments:
        # Experiment C: Existing data + diverse TTS positives
        if not NEW_DIVERSE_POSITIVES.exists():
            print(f"ERROR: Diverse positives not found at {NEW_DIVERSE_POSITIVES}")
            print("Run generate_training_data.py first.")
        else:
            result_c = train_and_evaluate(
                "C_diverse",
                pos_dirs=[EXISTING_POSITIVES, NEW_DIVERSE_POSITIVES],
                neg_dirs=[EXISTING_NEGATIVES],
                output_model=EXPERIMENTS_DIR / "models" / "exp_c_diverse.onnx",
                eval_dir=EVAL_DIR,
                epochs=args.epochs,
                patience=args.patience,
            )
            results.append(result_c)

    if "D" in experiments:
        # Experiment D: Combined
        has_neg = NEW_CONFUSABLE_NEGATIVES.exists()
        has_pos = NEW_DIVERSE_POSITIVES.exists()
        if not has_neg or not has_pos:
            missing = []
            if not has_neg:
                missing.append("confusable negatives")
            if not has_pos:
                missing.append("diverse positives")
            print(f"ERROR: Missing data for D: {', '.join(missing)}")
        else:
            result_d = train_and_evaluate(
                "D_combined",
                pos_dirs=[EXISTING_POSITIVES, NEW_DIVERSE_POSITIVES],
                neg_dirs=[EXISTING_NEGATIVES, NEW_CONFUSABLE_NEGATIVES],
                output_model=EXPERIMENTS_DIR / "models" / "exp_d_combined.onnx",
                eval_dir=EVAL_DIR,
                epochs=args.epochs,
                patience=args.patience,
            )
            results.append(result_d)

    if "E" in experiments:
        # Experiment E: MAX DATA — everything we can find
        # All positives: main + backup + eval_real + excluded + new TTS
        all_pos_dirs = [EXISTING_POSITIVES] + EXTRA_POS_DIRS
        if NEW_DIVERSE_POSITIVES.exists():
            all_pos_dirs.append(NEW_DIVERSE_POSITIVES)
        # All negatives: main + backup + eval_real + downloads + noise + music + new confusable
        all_neg_dirs = [EXISTING_NEGATIVES] + EXTRA_NEG_DIRS
        if NEW_CONFUSABLE_NEGATIVES.exists():
            all_neg_dirs.append(NEW_CONFUSABLE_NEGATIVES)

        result_e = train_and_evaluate(
            "E_maxdata",
            pos_dirs=all_pos_dirs,
            neg_dirs=all_neg_dirs,
            output_model=EXPERIMENTS_DIR / "models" / "exp_e_maxdata.onnx",
            eval_dir=EVAL_DIR,
            epochs=args.epochs,
            patience=args.patience,
        )
        results.append(result_e)

    # Print comparison
    print_comparison_table(results)

    # Save results
